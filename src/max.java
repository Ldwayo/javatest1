public class max {      //max help
    int a,b;
    public max(int a,int b){
        this.a = a;
        this.b = b;
    }
    public int max(){
        if(a>b)
            return a;
        else return b;
    }
    public int min(){
        if(a<b)
            return a;
        else return b;
    }
}
